// 클래스 이름은 첫글자를 대문자로 작성
public class MyMain {
	// main은 자바를 실행하기 위한 메서드 입니다.
	// 주석 단축키 : ctrl + /
	// 한줄 지우기 단축키 : ctrl + d
	// 줄정리 단축키 : ctrl + shift + f
	public static void main(String[] args) {
		// 콘솔창에 괄호안의 내용을 출력하는 메서드
		System.out.println("안녕하세요!!");
		System.out.println("오늘의 날씨는 흐립니다.");
		// syso를 적은 후 ctrl+space를 누르면 자동완성
		System.out.println();
	}
}
